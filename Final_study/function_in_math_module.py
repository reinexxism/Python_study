# math module의 함수 사용
import math as m

# 3의 3제곱을 구하는 함수 : .pow() function
a = m.pow(3, 3)
print(a)

# -99의 실수 절댓값을 구하는 함수 : .fabs() function
b = m.fabs(-99)
print(b)

# 2.1의 올림값을 구하는 함수 : .ceil() function
c = m.ceil(2.1)
print(c)

# -2.1의 올림값을 구하는 함수 : .ceil() function
d = m.ceil(-2.1)
print(d)

# 2.1의 내림값을 구하는 함수 : .floor() function
e = m.floor(2.1)
print(e)

# 2.17828의 log값을 구하는 함수 : .log() function
f = m.log(2.71828)
print(f)

# 로그 10을 밑으로 하는 100값 : .log() function
g = m.log(100, 10)
print(g)

